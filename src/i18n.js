export const i18nState = reactive({
  locale: localStorage.getItem('istar_locale') || 'th',
})

export function setLocale(lang) {
  i18nState.locale = lang
  localStorage.setItem('istar_locale', lang)
}

export function t(key, params) {
  const keys = key.split('.')
  let result = messages[i18nState.locale]
  for (const k of keys) {
    if (result == null) return key
    result = result[k]
  }
  if (result != null && params) {
    return result.replace(/\{(\w+)\}/g, (_, k) => (params[k] !== undefined ? params[k] : ''))
  }
  return result != null ? result : key
}
